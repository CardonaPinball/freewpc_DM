/****************************************************************************
 *
 * sound effects
 *
 ****************************************************************************/
void ramp_sounds (void) {
	U8 rand_sound;
	if (system_config.swear_sounds == YES) {
		NumRampsSounds = 29;
		//this doubles the chance that a swear would occur
		rand_sound = random_scaled(39);
		if (rand_sound >= 29) rand_sound -= 10;
	}
	else if (system_config.extra_sounds == YES) {
		NumRampsSounds = 19;
		rand_sound = random_scaled(NumRampsSounds);
	}
	else {
		NumRampsSounds = 18;
		rand_sound = random_scaled(NumRampsSounds);
	}

	if (++rampSoundsCounter % 3 == 0)
						sound_start (ST_SPEECH, rampSoundsArray[rand_sound], SL_4S, PRI_GAME_QUICK5);
	else if (rampSoundsCounter % 3 == 1)
						sound_start (ST_ANY,explosionSoundsArray[random_scaled(4)], SL_4S, PRI_GAME_QUICK5);
}//end of function



