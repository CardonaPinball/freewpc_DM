void right_ramp_made(void) {
	score (RAMPS_MADE_SCORE);
	if (get_back_in_the_fridge_running() ) 						back_in_the_fridge_shot_made();
	if (get_is_super_jackpot_lit() )						score_super_jackpot();
	if (get_jackpot_arrows(rramp_mask) ) 					score_jackpot();
	if (is_huxley_running ()  )							huxley_mode_shot_made();
	if (is_prison_break_running () ) 						prison_break_made();
	if (get_capture_simon_arrows (CAPSIM_RIGHTRAMP) )		capture_simon_made();

	if (flag_test (FLAG_IS_COMBO_RIGHTRAMP_ACTIVATED) ) 	combo_hit();
	else if (get_is_combos_killed ()
		&&	 get_is_combos_restartable () ) 						combo_init();

	//regardless of what else is going on, we can do these
	if (is_explode_mode_running()  )							 		explode_made(); //goto eyeball_explode.c for scoring
	if (is_carchase_mode_running()  )									car_chase_ramp_made(); //goto carchase.c for scoring

	//if claw is disabled then and claw ready then right ramp
	//acts like a claw hit
	if (	system_config.disable_claw == YES
		&&	flag_test(FLAG_IS_R_RAMP_CLAWREADY ) )			disabled_claw_hit ();



	//if not in a mode then perform normal sounds and display effects
	if (	!is_explode_mode_running()
		&& 	!is_carchase_mode_running()
		&& 	!get_capture_simon_arrows (CAPSIM_RIGHTRAMP) )
				ramp_sounds();
}//end of function


