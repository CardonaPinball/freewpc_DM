struct timed_mode_ops capture_simon_mode = {
	DEFAULT_MODE,
	.init = capture_simon_mode_init,
	.exit = capture_simon_mode_expire,
	.gid = GID_CAPTURE_SIMON_MODE_RUNNING,
	.music = MUS_MD_CAPTURE_SIMON,
	.deff_starting = DEFF_CAPTURE_SIMON_START_EFFECT,
	.deff_running = DEFF_CAPTURE_SIMON_EFFECT,
	.deff_ending = DEFF_CAPTURE_SIMON_END_EFFECT,
	.prio = PRI_GAME_MODE4,
	.init_timer = 48,
	.timer = &capture_simon_mode_timer,
	.grace_timer = 2,
	.pause = suspend_capture_simon,
};



/****************************************************************************
 * initialize  and exit
 ***************************************************************************/
void capture_simon_reset (void) {
	set_capture_simon_arrows_all_off ();
	is_cap_sim_initial_start = FALSE;

	if (get_dm_mode(CAP_SIM_COMPLETED) ) 	lamp_tristate_on (LM_CLAW_CAPTURE_SIMON);
	else 									lamp_tristate_off (LM_CLAW_CAPTURE_SIMON);
}//end of function


void capture_simon_player_reset (void) {
	capture_simon_mode_shots_made = 0;
	capture_simon_mode_shots_goal = CAP_SIM_EASY_GOAL;
	score_zero(capture_simon_mode_score_total_score);
	capture_simon_SoundCounter = 0;
	capture_simon_arrow = 0x0;
	capture_simon_suspend = FALSE;
}//end of function



//purpose here is to prevent underground from triggering since
//this shot feeds to underground
void capture_simon_startup_task (void) {
	is_cap_sim_initial_start = TRUE;
	task_sleep_sec(10);
	//this is just here in case deff hangs or does not play
	is_cap_sim_initial_start = FALSE;
	task_exit();
}//end of function




void capture_simon_mode_init (void) {
	capture_simon_suspend = FALSE;
	task_create_gid1(GID_CAP_SIM_START, capture_simon_startup_task);
	//the claw mode can expire on its own and since it is a lower priority it will not display
			//callset_invoke (end_claw_mode); // as this seemed to cause occasional crashes
			clawmagnet_off ();
			global_flag_off(GLOBAL_FLAG_IS_BALL_ON_CLAW);
			flipper_enable ();
			ballsave_add_time (system_config.mode_ballsave);
	capture_simon_mode_shots_made = 0;
	kill_combos ();
	//flash lamp for a time
	lamp_tristate_flash(LM_CLAW_CAPTURE_SIMON);
	task_sleep(TIME_500MS);
	lamp_tristate_on(LM_CLAW_CAPTURE_SIMON);
	//reset counter if first time run, otherwise do not - player may restart mode while currently running
	if (get_dm_mode(CAP_SIM_COMPLETED) ) 	{
		score_add(capture_simon_mode_next_score, score_table[CAPSIM_HIT_SCORE_2]);
	}
	else 									{
		score_zero(capture_simon_mode_score);
		score_zero(capture_simon_mode_last_score);
		score_zero(capture_simon_mode_next_score);
		score_add(capture_simon_mode_next_score, score_table[CAPSIM_HIT_SCORE_1]);
	}
	//this pause is here to give sounds time to finish
	task_sleep(TIME_500MS);
	task_sleep(TIME_500MS);
	task_sleep(TIME_500MS);
	cap_simon_choose_random_flag_set();
	all_arrow_update();
	demotime_increment();
}//end of function




//this is an exit on timeout, ball still in play
void capture_simon_mode_expire (void) {
	set_capture_simon_arrows_all_off ();
	all_arrow_update();
	is_cap_sim_initial_start = FALSE;

	capture_simon_mode_shots_goal += CAP_SIM_GOAL_INCREMENT;//increment goal for next time
	set_dm_mode(CAP_SIM_COMPLETED); //for next time

	combo_init ();
}//end of function



//this is an exit on end of ball
//must do this differently to prevent excessive cycling of diverter which
//tends to blow fuses
void capture_simon_mode_exit (void) {
		set_capture_simon_arrows_all_off ();
		all_arrow_update();
		is_cap_sim_initial_start = FALSE;

		capture_simon_mode_shots_goal += CAP_SIM_GOAL_INCREMENT;//increment goal for next time
		set_dm_mode(CAP_SIM_COMPLETED); //for next time

		timed_mode_end2 (&capture_simon_mode);
	}//end of function



