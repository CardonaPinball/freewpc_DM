void capture_simon_effect_deff(void) {
	U8 i = 0;
	U8 j = 0;
	__boolean TOGGLE = FALSE;
	__boolean TOGGLE_BOTTOM = FALSE;

	for (;;) {
		dmd_alloc_pair_clean();
//right side status display
		ll_score_draw_ball ();

//left side display
		bitmap_blit (cuffs_3_bits, 0, 16);
		if (++i % 2 == 0) { if (TOGGLE) TOGGLE = FALSE; else TOGGLE = TRUE; }//change TOGGLE once per second
		if (++j % 8 == 0) { if (TOGGLE_BOTTOM) TOGGLE_BOTTOM = FALSE; else TOGGLE_BOTTOM = TRUE; }//change TOGGLE once per second

		if (TOGGLE) font_render_string_right (&font_halobold12, 98, 1, "CATCH");
		else 		font_render_string_right (&font_halobold12, 98, 1, "SIMON");

		if (ballsave_test_active()) {
			sprintf ("%d BALL SAVE", ballsave_get_timer());
			font_render_string_center (&font_var5, 50, DMD_MED_CY_BOTTOM_LINE + 3, sprintf_buffer);
		}
		else
			if (TOGGLE_BOTTOM) {
			sprintf ("%d SEC", capture_simon_mode_timer, capture_simon_mode_shots_made);
			font_render_string_center (&font_var5, 72, DMD_SMALL_CY_3 + 1, sprintf_buffer);
			sprintf_score (capture_simon_mode_next_score);
			font_render_string_right (&font_term6, 98, 23, sprintf_buffer);
		}
		else {
			font_render_string_center (&font_term6, 72, DMD_MED_CY_2 + 2, "SHOOT");
			font_render_string_center (&font_term6, 72, DMD_MED_CY_BOTTOM_LINE, "ARROWS");
		}

		dmd_show_low ();
		task_sleep (TIME_1S);
	}//END OF ENDLESS LOOP
}//end of mode_effect_deff





/****************************************************************************
 *
 * randomize flag chosen
 *
 * we are using a very stupid and in-elegant
 * way of randomness - basically, we have a number
 * of predefined sets of flags and we randomly
 * pick one of those sets
 *
 * We do this mainly because there are certain combinations that
 * I do not want to be chosen
 ****************************************************************************/
void cap_simon_choose_random_flag_set(void) {
	U8 random_chooser;
	random_chooser = random_scaled(6);
	switch (random_chooser) {
	default:
	case 0:
		set_capture_simon_arrows_all_off ();
		set_capture_simon_arrows (CAPSIM_SIDERAMP + CAPSIM_LEFTRAMP + CAPSIM_CENTERRAMP); //123
		break;
	case 1:
		set_capture_simon_arrows_all_off ();
		set_capture_simon_arrows (CAPSIM_RIGHTRAMP + CAPSIM_LEFTRAMP + CAPSIM_CENTERRAMP); //124
		break;
	case 2:
		set_capture_simon_arrows_all_off ();
		set_capture_simon_arrows (CAPSIM_LEFTRAMP + CAPSIM_UNDERGRND + CAPSIM_CENTERRAMP); //125
		break;
	case 3:
		set_capture_simon_arrows_all_off ();
		set_capture_simon_arrows (CAPSIM_SIDERAMP + CAPSIM_CENTERRAMP + CAPSIM_RIGHTRAMP); //234
		break;
	case 4:
		set_capture_simon_arrows_all_off ();
		set_capture_simon_arrows (CAPSIM_SIDERAMP + CAPSIM_CENTERRAMP + CAPSIM_UNDERGRND); //235
		break;
	case 5:
		set_capture_simon_arrows_all_off ();
		set_capture_simon_arrows (CAPSIM_SIDERAMP +  CAPSIM_RIGHTRAMP + CAPSIM_UNDERGRND); //345
		break;
	}//end of switch
}//end of function

