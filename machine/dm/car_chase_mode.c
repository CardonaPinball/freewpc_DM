void car_chase_hit_effect_deff(void) {
	U8 car_chase_SoundCounter;

	if (system_config.swear_sounds == YES) {
		car_chase_TotalNumOfSounds = 16;
		//this doubles the chance that a swear would occur
		car_chase_SoundCounter = random_scaled (20);
		if (car_chase_SoundCounter >= 16) car_chase_SoundCounter -= 4;
	}
	else if (system_config.extra_sounds == YES) {
		car_chase_TotalNumOfSounds = 12;
		car_chase_SoundCounter = random_scaled(car_chase_TotalNumOfSounds);
	}
	else {
		car_chase_TotalNumOfSounds = 9;
		car_chase_SoundCounter = random_scaled(car_chase_TotalNumOfSounds);
	}


	dmd_clean_page_high ();
	dmd_clean_page_low ();

	//NOTE: skid sound is played on ramp enter at ramps.c
	//these sounds are on ramp exit
	sound_start (ST_SPEECH, car_chase_SoundsArray[car_chase_SoundCounter], SL_4S, PRI_GAME_QUICK3);
	sound_start (ST_EFFECT, CAR_GEAR_CHANGE, SL_2S, PRI_GAME_QUICK3);

	if (DM_IN_DMD_TEST) {	if (++carchase_MessageCounter > 8) carchase_MessageCounter = 0; }
	else			carchase_MessageCounter = random_scaled(9);


	switch (carchase_MessageCounter) {
		default:
		case 0:
			car_chase_animation_display_effect (IMG_CARCHASE_B_START, IMG_CARCHASE_B_END);
			car_chase_animation_display_effect (IMG_CARCHASE_A_START, IMG_CARCHASE_A_END);
			break;
		case 1:
			car_chase_animation_display_effect (IMG_CARCHASE_C_START, IMG_CARCHASE_C_END);
			car_chase_animation_display_effect (IMG_CARCHASE_B_START, IMG_CARCHASE_B_END);
			break;
		case 2:
			car_chase_animation_display_effect (IMG_CARCHASE_D_START, IMG_CARCHASE_D_END);
			car_chase_animation_display_effect (IMG_CARCHASE_C_START, IMG_CARCHASE_C_END);
			break;
		case 3:
			car_chase_animation_display_effect (IMG_CARCHASE_A_START, IMG_CARCHASE_A_END);
			car_chase_animation_display_effect (IMG_CARCHASE_D_START, IMG_CARCHASE_D_END);
			break;
		case 4:
			car_chase_animation_display_effect (IMG_CARCHASE_F1_START, IMG_CARCHASE_F1_END);
			car_chase_animation_display_effect (IMG_CARCHASE_E_START, IMG_CARCHASE_E_END);
			break;
		case 5:
			car_chase_animation_display_effect (IMG_CARCHASE_F2_START, IMG_CARCHASE_F2_END);
			car_chase_animation_display_effect (IMG_CARCHASE_J_START, IMG_CARCHASE_J_END);
			break;
		case 6:
			car_chase_animation_display_effect (IMG_CARCHASE_F2_START, IMG_CARCHASE_F2_END);
			car_chase_animation_display_effect (IMG_CARCHASE_F1_START, IMG_CARCHASE_F1_END);
			break;
		case 7:
			car_chase_animation_display_effect (IMG_CARCHASE_A_START, IMG_CARCHASE_A_END);
			car_chase_animation_display_effect (IMG_CARCHASE_J_START, IMG_CARCHASE_J_END);
			break;
		case 8:
			car_chase_animation_display_effect (IMG_CARCHASE_J_START, IMG_CARCHASE_J_END);
			car_chase_animation_display_effect (IMG_CARCHASE_E_START, IMG_CARCHASE_E_END);
			break;
		}//end of switch
	task_sleep (TIME_100MS);
	deff_exit ();
}//end of function



void car_chase_effect_deff(void) {
	U8 i = 0;
	for (;;) {
		dmd_clean_page_low ();
		if (++i % 4 == 0) 	{ //do these once every xx seconds
			leff_start (LEFF_CAR_CHASE);
			sound_start (ST_SAMPLE, CAR_SKID, SL_2S, PRI_GAME_QUICK3);
			dmd_sched_transition (&trans_scroll_left_fast);
		}
//right side status display
		ll_score_draw_ball ();

		//left side display
		sprintf_score (car_chase_mode_score);
		font_render_string_center (&font_fixed6, 50, 15, sprintf_buffer);
		font_render_string_right (&font_halobold12, 84, 1, "CHASE");
		if (ballsave_test_active()) sprintf ("%d BALL SAVE", ballsave_get_timer());
		else sprintf ("%d SEC LEFT,  %d HIT", car_chase_mode_timer, car_chase_mode_shots_made);
		font_render_string_right (&font_var5, 88, 27, sprintf_buffer);
		dmd_show_low ();
		task_sleep (TIME_1S);
	}//END OF ENDLESS LOOP
}//end of mode_effect_deff





