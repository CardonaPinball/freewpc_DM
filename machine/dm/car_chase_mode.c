void car_chase_animation_display_effect (U16 start_frame, U16 end_frame){
	U16 fno;
	dmd_alloc_pair_clean ();// Clean both pages
	for (fno = start_frame; fno <= end_frame; fno += 2) {
		dmd_alloc_pair ();
		frame_draw(fno);
		dmd_show2 ();
		task_sleep (TIME_100MS);
	}//end of inner loop
}



